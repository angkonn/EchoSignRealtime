#pragma once
#include <Arduino.h>

#define NUM_FEATURES 12

static const float SCALER_MEAN[NUM_FEATURES] = { 2556.134033f, 2242.419109f, 2477.859883f, 2443.640986f, 2428.269556f, 3450.713398f, -990.333499f, 12798.366153f, 2491.682394f, 580.148684f, 76.875776f, 55.536628f };
static const float SCALER_SCALE[NUM_FEATURES] = { 204.086796f, 551.048373f, 191.521424f, 201.898458f, 211.732238f, 4275.146597f, 7609.698604f, 4297.442616f, 5137.421480f, 2459.483168f, 4325.140695f, 2254.514437f };

inline void standardizeFeatures(float feat[NUM_FEATURES]) {
  for (int i = 0; i < NUM_FEATURES; ++i) {
    feat[i] = (feat[i] - SCALER_MEAN[i]) / SCALER_SCALE[i];
  }
}
